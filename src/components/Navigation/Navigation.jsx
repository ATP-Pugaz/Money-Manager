const navItems = [
    { id: 'home', label: 'Home', Icon: HomeIcon },
    { id: 'calendar', label: 'Calendar', Icon: CalendarIcon },
    { id: 'analytics', label: 'Analytics', Icon: AnalyticsIcon },
    { id: 'transactions', label: 'Transactions', Icon: TransactionsIcon },
    { id: 'settings', label: 'Settings', Icon: SettingsIcon },
];

export default function Navigation() {
    const { activeTab, setActiveTab, getAutoSyncStats } = useApp();
    const stats = getAutoSyncStats();

    return (
        <nav className="nav-bar">
            {navItems.map(({ id, label, Icon }) => (
                <button
                    key={id}
                    className={`nav-item ${activeTab === id ? 'active' : ''} ${id === 'settings' ? 'settings-icon' : ''}`}
                    onClick={() => setActiveTab(id)}
                    aria-label={label}
                >
                    <span className="nav-icon">
                        <Icon />
                    </span>
                    <span className="nav-label">{label}</span>

                    {/* Show badge for pending transactions on transactions tab */}
                    {id === 'transactions' && stats.pendingCount > 0 && (
                        <span className="nav-badge">{stats.pendingCount}</span>
                    )}
                </button>
            ))}
        </nav>
    );
}
